# Demonstrate the use of Assignment operator
# Addition
a = 5
a += 3  # Equivalent to> a = a+3
print("Result after addition:", a)

# Subtraction
b = 10
b -= 4
print("Result after subtraction:", b)

# Multiplication
c = 3
c *= 2
print("Result after multiplication:", c)

# Division
d = 8
d /= 4
print("Result after division:", d)
